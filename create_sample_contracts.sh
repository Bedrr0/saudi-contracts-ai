PARTNERSHIP CONTRACT

This Partnership Contract (the "Contract") is made and entered into on 30/04/2025, by and between:

FIRST PARTNER: Mohammed Al-Qahtani, Saudi national, ID No. 1065432109, residing at Al Nakheel District, Riyadh, Saudi Arabia (hereinafter referred to as the "First Partner")

AND

SECOND PARTNER: Khalid Al-Otaibi, Saudi national, ID No. 1054321098, residing at Al Yasmin District, Riyadh, Saudi Arabia (hereinafter referred to as the "Second Partner")

The First Partner and Second Partner shall collectively be referred to as the "Partners".

1. PARTNERSHIP NAME AND TYPE
   1.1 The Partners hereby form a general partnership under the name "Al-Qahtani & Al-Otaibi Consulting Partners" (hereinafter referred to as the "Partnership").
   1.2 The Partnership shall be registered as a general partnership (Sharikat Tadamun) in accordance with the Saudi Companies Law.

2. PARTNERSHIP PURPOSE
   The purpose of the Partnership is to provide management consulting services to businesses in the Kingdom of Saudi Arabia.

3. PARTNERSHIP TERM
   The Partnership shall commence on 01/06/2025 and shall continue for a period of 5 years, unless terminated earlier in accordance with the provisions of this Contract.

4. PARTNERSHIP CAPITAL
   4.1 The capital of the Partnership shall be SAR 1,000,000 (One Million Saudi Riyals).
   4.2 The Partners shall contribute to the capital as follows:
       - First Partner: SAR 600,000 (60% of the capital)
       - Second Partner: SAR 400,000 (40% of the capital)
   4.3 The capital contributions shall be paid in cash to the Partnership's bank account within 30 days from the date of registration of the Partnership.

5. PROFIT AND LOSS DISTRIBUTION
   5.1 The profits and losses of the Partnership shall be distributed between the Partners in proportion to their capital contributions.
   5.2 The First Partner shall receive 60% of the profits and bear 60% of the losses.
   5.3 The Second Partner shall receive 40% of the profits and bear 40% of the losses.

6. MANAGEMENT
   6.1 Both Partners shall participate in the management of the Partnership.
   6.2 The First Partner shall be the Managing Partner and shall be responsible for the day-to-day operations of the Partnership.
   6.3 The following decisions shall require the unanimous approval of both Partners:
       - Borrowing money on behalf of the Partnership
       - Selling or mortgaging Partnership assets
       - Admitting new partners
       - Changing the nature of the Partnership's business
       - Amending this Contract

7. PARTNER DUTIES
   7.1 Each Partner shall devote their full time and attention to the business of the Partnership.
   7.2 Neither Partner shall engage in any other business that competes with the Partnership without the prior written consent of the other Partner.

8. BANKING
   8.1 The Partnership shall maintain a bank account in its name.
   8.2 Both Partners shall be signatories to the bank account.
   8.3 All Partnership funds shall be deposited in the Partnership's bank account.

9. ACCOUNTING
   9.1 The Partnership shall maintain proper books of accounts.
   9.2 The fiscal year of the Partnership shall be the Hijri calendar year.
   9.3 Financial statements shall be prepared at the end of each fiscal year.
   9.4 Each Partner shall have access to the Partnership's books and records at all reasonable times.

10. TRANSFER OF PARTNERSHIP INTEREST
    10.1 Neither Partner shall sell, assign, or transfer their interest in the Partnership without first offering it to the other Partner.
    10.2 The other Partner shall have the right of first refusal to purchase the interest at fair market value.

11. WITHDRAWAL OR DEATH OF A PARTNER
    11.1 In the event of the withdrawal or death of a Partner, the Partnership shall not dissolve.
    11.2 The remaining Partner shall have the option to purchase the interest of the withdrawing or deceased Partner at fair market value.

12. DISPUTE RESOLUTION
    Any dispute arising out of or in connection with this Contract shall be resolved through arbitration in accordance with the Saudi Arbitration Law.

13. GOVERNING LAW
    This Contract shall be governed by and construed in accordance with the laws of the Kingdom of Saudi Arabia, including the Saudi Companies Law.

IN WITNESS WHEREOF, the Partners have executed this Contract as of the date first above written.

FIRST PARTNER                          SECOND PARTNER
Mohammed Al-Qahtani                    Khalid Al-Otaibi
_____________________                 _____________________
Signature                              Signature
EOL

