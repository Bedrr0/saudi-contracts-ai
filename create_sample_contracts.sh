EMPLOYMENT CONTRACT

This Employment Contract (the "Contract") is made and entered into on 15/04/2025, by and between:

EMPLOYER: Saudi Company LLC, Commercial Registration No. 1234567890, having its principal place of business at Riyadh Business District, Riyadh, Saudi Arabia (hereinafter referred to as the "Employer")

AND

EMPLOYEE: Ahmed Abdullah, ID No. 1098765432, residing at Al Olaya District, Riyadh, Saudi Arabia (hereinafter referred to as the "Employee")

The Employer and Employee shall collectively be referred to as the "Parties".

1. JOB TITLE AND DESCRIPTION
   The Employee is hired for the position of Marketing Manager. The Employee shall be responsible for developing and implementing marketing strategies, managing the marketing team, and reporting to the Chief Marketing Officer.

2. COMMENCEMENT AND DURATION
   This Contract shall commence on 01/05/2025 and shall continue for an indefinite period unless terminated in accordance with the provisions of this Contract.

3. PROBATION PERIOD
   The Employee shall be subject to a probation period of 90 days from the commencement date. During this period, either Party may terminate this Contract without notice or compensation.

4. WORKING HOURS
   The Employee shall work 9 hours per day, from Sunday to Thursday, from 8:00 AM to 5:00 PM, with a one-hour break for lunch and prayers.

5. SALARY AND BENEFITS
   5.1 The Employer shall pay the Employee a monthly salary of SAR 15,000.
   5.2 The salary shall be paid on the last day of each month through bank transfer.
   5.3 The Employee shall be entitled to an annual performance bonus as determined by the Employer.

6. ANNUAL LEAVE
   The Employee shall be entitled to 21 days of paid annual leave per year. The timing of the leave shall be determined by mutual agreement between the Parties.

7. SICK LEAVE
   The Employee shall be entitled to sick leave in accordance with the Saudi Labor Law.

8. TERMINATION
   8.1 Either Party may terminate this Contract by providing 60 days' written notice to the other Party.
   8.2 The Employer may terminate this Contract without notice in case of gross misconduct by the Employee.

9. END OF SERVICE BENEFITS
   Upon termination of this Contract, the Employee shall be entitled to end of service benefits in accordance with the Saudi Labor Law.

10. NON-COMPETE
    The Employee agrees not to engage in any competing business or activity for a period of 3 years following the termination of this Contract.

11. CONFIDENTIALITY
    The Employee shall maintain the confidentiality of all proprietary information of the Employer during and after the term of this Contract.

12. GOVERNING LAW
    This Contract shall be governed by and construed in accordance with the laws of the Kingdom of Saudi Arabia, including the Saudi Labor Law.

13. DISPUTE RESOLUTION
    Any dispute arising out of or in connection with this Contract shall be resolved through the competent labor courts in the Kingdom of Saudi Arabia.

IN WITNESS WHEREOF, the Parties have executed this Contract as of the date first above written.

EMPLOYER                                EMPLOYEE
Saudi Company LLC                       Ahmed Abdullah
_____________________                  _____________________
Authorized Signatory                    Signature
