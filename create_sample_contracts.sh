SALES CONTRACT

This Sales Contract (the "Contract") is made and entered into on 25/04/2025, by and between:

SELLER: Saudi Electronics Company, Commercial Registration No. 2345678901, VAT Registration No. 123456789012345, having its principal place of business at King Fahd Road, Riyadh, Saudi Arabia (hereinafter referred to as the "Seller")

AND

BUYER: Riyadh Office Supplies LLC, Commercial Registration No. 3456789012, having its principal place of business at Olaya Street, Riyadh, Saudi Arabia (hereinafter referred to as the "Buyer")

The Seller and Buyer shall collectively be referred to as the "Parties".

1. GOODS DESCRIPTION
   The Seller hereby agrees to sell and the Buyer agrees to purchase the following goods (hereinafter referred to as the "Goods"):
   - 20 units of Laptop Model XYZ-2025
   - 20 units of Desktop Computer Model ABC-2025
   - 40 units of Monitor Model DEF-2025

2. PRICE
   2.1 The price for the Goods shall be SAR 200,000 (Two Hundred Thousand Saudi Riyals).
   2.2 The price is exclusive of Value Added Tax (VAT).
   2.3 VAT at the rate of 15% (SAR 30,000) shall be added to the price.
   2.4 The total amount payable including VAT shall be SAR 230,000 (Two Hundred Thirty Thousand Saudi Riyals).

3. PAYMENT TERMS
   3.1 The Buyer shall pay 50% of the total amount (SAR 115,000) as an advance payment upon signing this Contract.
   3.2 The remaining 50% (SAR 115,000) shall be paid within 30 days after delivery of the Goods.
   3.3 Payment shall be made by bank transfer to the Seller's account.

4. DELIVERY
   4.1 The Seller shall deliver the Goods to the Buyer's premises at Olaya Street, Riyadh, Saudi Arabia.
   4.2 The delivery shall be made within 14 days from the date of the advance payment.
   4.3 The risk of loss or damage to the Goods shall pass to the Buyer upon delivery.

5. INSPECTION
   The Buyer shall inspect the Goods upon delivery and notify the Seller of any defects or non-conformity within 7 days after delivery.

6. WARRANTY
   6.1 The Seller warrants that the Goods are free from defects in materials and workmanship.
   6.2 The warranty period shall be 12 months from the date of delivery.
   6.3 The warranty does not cover damage resulting from misuse, negligence, or unauthorized repair.

7. INVOICE
   The Seller shall issue a tax invoice to the Buyer containing the following information:
   - Invoice date and number
   - Seller's name, address, and VAT registration number
   - Buyer's name and address
   - Description of the Goods
   - Price excluding VAT
   - VAT amount
   - Total price including VAT

8. TERMINATION
   Either Party may terminate this Contract if the other Party breaches any material term of this Contract and fails to remedy such breach within 14 days after receiving written notice.

9. GOVERNING LAW
   This Contract shall be governed by and construed in accordance with the laws of the Kingdom of Saudi Arabia.

10. DISPUTE RESOLUTION
    Any dispute arising out of or in connection with this Contract shall be resolved through the competent commercial courts in the Kingdom of Saudi Arabia.

IN WITNESS WHEREOF, the Parties have executed this Contract as of the date first above written.

